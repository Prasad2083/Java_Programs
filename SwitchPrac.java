public class SwitchPrac {
    public static void main(String[] args) {
        int i=1;
        switch (i){
            case 1:
                System.out.println("This is 1");
                break;
            case 2:
                System.out.println("2");
                break;
            case 3:
                System.out.println("3");
                break;
        }
    }
}
